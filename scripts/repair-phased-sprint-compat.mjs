import dotenv from "dotenv";
import { createClient } from "@supabase/supabase-js";
import { deriveMilestoneEvidenceRequirements, resolveMilestoneCheckpointType } from "../src/lib/milestone-review.ts";

dotenv.config({ path: ".env.local", quiet: true });

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !serviceRoleKey) {
  throw new Error("Supabase env is required");
}

const supabase = createClient(supabaseUrl, serviceRoleKey);
const dryRun = process.argv.includes("--dry-run");

function inferPhaseFromName(name) {
  const normalized = String(name || "").toLowerCase();
  if (normalized.includes("discover")) return { key: "discover", order: 1 };
  if (normalized.includes("design")) return { key: "design", order: 2 };
  if (normalized.includes("build")) return { key: "build", order: 2 };
  if (normalized.includes("message")) return { key: "message", order: 3 };
  if (normalized.includes("validate")) return { key: "validate", order: 3 };
  return { key: null, order: null };
}

function inferPhaseOrder(name, phaseKey) {
  const numbered = String(name || "").match(/^Phase\s+(\d+)\b/i);
  if (numbered) return Number(numbered[1]);
  return inferPhaseFromName(name).order ?? (phaseKey === "build" ? 2 : phaseKey === "validate" ? 3 : phaseKey === "design" ? 2 : phaseKey === "message" ? 3 : phaseKey === "discover" ? 1 : null);
}

function inferCheckpointType({ currentCheckpointType, phaseKey, sprintName, taskTypes }) {
  const resolved = resolveMilestoneCheckpointType({
    checkpointType: currentCheckpointType,
    sprintName,
    phaseKey,
    taskTypes,
  });
  if (resolved && !(resolved === "delivery_review" && phaseKey && phaseKey !== "build")) return resolved;
  if (phaseKey === "discover") return "scope_approval";
  if (phaseKey === "design") return "design_review";
  if (phaseKey === "message") return "content_review";
  if (phaseKey === "validate") return "acceptance_review";
  if (phaseKey === "build") return "delivery_review";
  return currentCheckpointType ?? null;
}

const { data: sprints, error: sprintError } = await supabase
  .from("sprints")
  .select("id, project_id, name, phase_key, phase_order, auto_generated, checkpoint_type, checkpoint_evidence_requirements, delivery_review_required, delivery_review_status");
if (sprintError) throw sprintError;

const projectIds = Array.from(new Set((sprints || []).map((sprint) => sprint.project_id).filter(Boolean)));
const { data: projects, error: projectError } = projectIds.length === 0
  ? { data: [], error: null }
  : await supabase
      .from("projects")
      .select("id, type, intake")
      .in("id", projectIds);
if (projectError) throw projectError;

const { data: tasks, error: taskError } = await supabase
  .from("sprint_items")
  .select("id, sprint_id, task_type, task_metadata")
  .not("sprint_id", "is", null);
if (taskError) throw taskError;

const tasksBySprintId = new Map();
for (const task of tasks || []) {
  const rows = tasksBySprintId.get(task.sprint_id) || [];
  rows.push(task);
  tasksBySprintId.set(task.sprint_id, rows);
}

const projectsById = new Map((projects || []).map((project) => [project.id, project]));

const repairs = [];
for (const sprint of sprints || []) {
  const sprintTasks = tasksBySprintId.get(sprint.id) || [];
  const taskPhaseKey = sprintTasks.find((task) => typeof task.task_metadata?.phase_key === "string")?.task_metadata?.phase_key ?? null;
  const phaseKey = sprint.phase_key ?? taskPhaseKey ?? inferPhaseFromName(sprint.name).key;
  const phaseOrder = sprint.phase_order ?? inferPhaseOrder(sprint.name, phaseKey);
  const taskTypes = sprintTasks.map((task) => task.task_type).filter(Boolean);
  const checkpointType = inferCheckpointType({
    currentCheckpointType: sprint.checkpoint_type,
    sprintName: sprint.name,
    phaseKey,
    taskTypes,
  });
  const deliveryReviewRequired = phaseKey === "build" ? true : sprint.delivery_review_required ?? false;
  const deliveryReviewStatus = phaseKey === "build" ? sprint.delivery_review_status ?? "not_requested" : sprint.delivery_review_status ?? null;
  const autoGenerated = sprint.auto_generated === true ? true : sprintTasks.some((task) => task.task_metadata?.auto_generated === true) || Boolean(phaseKey);
  const project = projectsById.get(sprint.project_id) || null;
  const checkpointEvidenceRequirements = deriveMilestoneEvidenceRequirements({
    checkpointType,
    explicitRequirements: sprint.checkpoint_evidence_requirements,
    sprintName: sprint.name,
    phaseKey,
    taskTypes,
    projectType: project?.type ?? null,
    projectIntake: project?.intake ?? null,
  });

  const currentEvidenceRequirements = sprint.checkpoint_evidence_requirements && typeof sprint.checkpoint_evidence_requirements === "object"
    ? JSON.stringify(sprint.checkpoint_evidence_requirements)
    : null;
  const nextEvidenceRequirements = JSON.stringify(checkpointEvidenceRequirements);

  const needsRepair = sprint.phase_key !== phaseKey
    || sprint.phase_order !== phaseOrder
    || sprint.auto_generated !== autoGenerated
    || sprint.checkpoint_type !== checkpointType
    || sprint.delivery_review_required !== deliveryReviewRequired
    || sprint.delivery_review_status !== deliveryReviewStatus
    || currentEvidenceRequirements !== nextEvidenceRequirements;

  if (!needsRepair) continue;

  const patch = {
    phase_key: phaseKey,
    phase_order: phaseOrder,
    auto_generated: autoGenerated,
    checkpoint_type: checkpointType,
    checkpoint_evidence_requirements: checkpointEvidenceRequirements,
    delivery_review_required: deliveryReviewRequired,
    delivery_review_status: deliveryReviewStatus,
    updated_at: new Date().toISOString(),
  };

  if (!dryRun) {
    const { error } = await supabase.from("sprints").update(patch).eq("id", sprint.id);
    if (error) throw error;
  }
  repairs.push({ id: sprint.id, name: sprint.name, patch });
}

console.log(JSON.stringify({ dryRun, repaired: repairs.length, repairs }, null, 2));
