import assert from "node:assert/strict";
import { deriveProjectDetailHeaderState, deriveMilestoneDisplayState, getCompletedProjectRevisionMilestones, shouldShowReviewCheckpointSection } from "../src/lib/project-detail-state.ts";

const project = {
  status: "completed",
  progress_pct: 100,
  intake: { shape: "web-app", capabilities: ["frontend"] },
};

const completedTruth = {
  progressPct: 100,
  headline: "Project completed",
  summary: "This project is complete. You can still request a revision later if follow-up changes are needed.",
  execution: {
    key: "completed",
    label: "Completed",
    description: "This project is finished and closed out.",
  },
};

const buildMilestone = {
  id: "sprint-build",
  name: "Build",
  status: "completed",
  phaseKey: "build",
  phaseOrder: 1,
  autoGenerated: true,
  approvalGateRequired: false,
  approvalGateStatus: "approved",
  deliveryReviewRequired: true,
  deliveryReviewStatus: "approved",
  totalTasks: 2,
  doneTasks: 2,
  queuedTasks: 0,
  runningTasks: 0,
  blockedTasks: 0,
  progressPct: 100,
  reviewArtifacts: [],
  reviewRequest: null,
  reviewSummary: {
    latestSubmissionId: "submission-build-1",
    latestSubmissionStatus: "approved",
    latestRevisionNumber: 1,
    checkpointType: "delivery_review",
    latestSubmissionSummary: "Completed build submitted for delivery review",
    latestDecision: null,
    latestDecisionNotes: null,
    latestRejectionComment: null,
    proofItemCount: 0,
    proofCompletenessStatus: "ready",
    feedbackItemCount: 0,
  },
};

const headerState = deriveProjectDetailHeaderState({
  projectProgressPct: project.progress_pct,
  truth: completedTruth,
  attachmentKickoffState: project.intake?.attachmentKickoffState,
});
const milestoneState = deriveMilestoneDisplayState(buildMilestone);
const completedRevisionMilestones = getCompletedProjectRevisionMilestones([buildMilestone]);
const reviewableMilestones = [buildMilestone];

assert.equal(headerState.key, "completed", "Header should resolve to completed for completed projects");
assert.equal(headerState.progressPct, 100, "Header should show 100% progress for completed projects");
assert.equal(headerState.badgeText, "100% complete", "Header badge should stay completion-based");
assert.ok(/project completed/i.test(String(headerState.headline || "")), "Header should clearly say the project is completed");
assert.equal(milestoneState.stageState.key, "iteration_shipped", "Approved completed delivery milestone should be treated as shipped");
assert.equal(shouldShowReviewCheckpointSection({ projectStatus: project.status, milestones: reviewableMilestones }), false, "Completed projects should hide the review checkpoint section");
assert.equal(completedRevisionMilestones.length, 1, "Completed projects should still surface a revision-request path");

console.log("verify-completed-project-detail-state: ok", JSON.stringify({
  headerState,
  milestoneStage: milestoneState.stageState,
  completedRevisionMilestones: completedRevisionMilestones.map((milestone) => ({ id: milestone.id, name: milestone.name })),
}, null, 2));
