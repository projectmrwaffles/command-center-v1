function formatReviewStatus(value?: string | null) {
  if (!value || value === "not_requested") return "No review started";
  return value.replace(/_/g, " ");
}

function formatMilestoneGateLabel(value?: string | null) {
  if (!value || value === "not_requested") return "No checkpoint review";
  if (value === "pending") return "Awaiting review";
  if (value === "approved") return "Approved";
  if (value === "rejected") return "Changes requested";
  return value.replace(/_/g, " ");
}

function checkpointTone(value?: string | null) {
  switch (value) {
    case "approved":
      return "border-emerald-200 bg-emerald-50 text-emerald-700";
    case "pending":
      return "border-amber-200 bg-amber-50 text-amber-700";
    case "rejected":
      return "border-red-200 bg-red-50 text-red-700";
    default:
      return "border-zinc-200 bg-zinc-100 text-zinc-600";
  }
}

function proofTone(value?: string | null) {
  switch (value) {
    case "ready":
      return "border-emerald-200 bg-emerald-50 text-emerald-700";
    case "needs_update":
      return "border-amber-200 bg-amber-50 text-amber-700";
    default:
      return "border-zinc-200 bg-zinc-100 text-zinc-600";
  }
}

function formatTaskStatusLabel(value?: string | null) {
  if (!value) return "Unknown";
  return value.replace(/_/g, " ");
}

function taskProgressValue(task: any) {
  if (!task) return null;

  if (task.status === "done" || task.status === "completed") {
    return 100;
  }

  if (typeof task?.progress_pct === "number" && Number.isFinite(task.progress_pct)) {
    return Math.max(0, Math.min(100, task.progress_pct));
  }

  return null;
}

function isBootstrapTask(task: any, bootstrapSprintIds?: ReadonlySet<string>) {
  return matchesBootstrapTruth(task, bootstrapSprintIds);
}

function formatBytes(value?: number | null) {
  if (!value) return null;
  if (value < 1024) return `${value} B`;
  if (value < 1024 * 1024) return `${Math.round(value / 1024)} KB`;
  return `${(value / (1024 * 1024)).toFixed(1)} MB`;
}

function artifactEmptyState(projectType?: string | null, intake?: any) {
  if (intake?.shape === "launch-campaign" || ["marketing_growth", "marketing"].includes(projectType || "")) {
    return "No campaign links or creative artifacts added yet.";
  }
  if (["product_build", "ops_enablement", "saas", "web_app", "native_app"].includes(projectType || "")) {
    return "No repo, environment, or build links added yet.";
  }
  return "No project links or artifacts added yet.";
}

function formatUpdatedDate(value?: string | null) {
  if (!value) return "Recently updated";

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "Recently updated";

  return `Updated ${date.toLocaleDateString(undefined, {
    month: "short",
    day: "numeric",
    year: "numeric",
  })}`;
}

