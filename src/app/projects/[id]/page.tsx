function TaskStatusBadge({ status }: { status: string }) {
  const styles: Record<string, string> = {
    todo: "border-zinc-200 bg-zinc-100 text-zinc-600",
    in_progress: "border-blue-200 bg-blue-100 text-blue-700",
    review: "border-purple-200 bg-purple-100 text-purple-700",
    done: "border-emerald-200 bg-emerald-100 text-emerald-700",
    blocked: "border-red-200 bg-red-100 text-red-700",
    cancelled: "border-slate-200 bg-slate-100 text-slate-600",
  };
  return (
    <span className={cn("rounded-full border px-2.5 py-1 text-[10px] font-medium uppercase tracking-[0.12em]", styles[status] || styles.todo)}>
      {status.replace("_", " ")}
    </span>
  );
}

function formatReviewStatus(value?: string | null) {
  if (!value || value === "not_requested") return "No review started";
  return value.replace(/_/g, " ");
}

function formatMilestoneGateLabel(value?: string | null) {
  if (!value || value === "not_requested") return "No checkpoint review";
  if (value === "pending") return "Awaiting review";
  if (value === "approved") return "Approved";
  if (value === "rejected") return "Changes requested";
  return value.replace(/_/g, " ");
}

function checkpointTone(value?: string | null) {
  switch (value) {
    case "approved":
      return "border-emerald-200 bg-emerald-50 text-emerald-700";
    case "pending":
      return "border-amber-200 bg-amber-50 text-amber-700";
    case "rejected":
      return "border-red-200 bg-red-50 text-red-700";
    default:
      return "border-zinc-200 bg-zinc-100 text-zinc-600";
  }
}

function proofTone(value?: string | null) {
  switch (value) {
    case "ready":
      return "border-emerald-200 bg-emerald-50 text-emerald-700";
    case "needs_update":
      return "border-amber-200 bg-amber-50 text-amber-700";
    default:
      return "border-zinc-200 bg-zinc-100 text-zinc-600";
  }
}

function isBlockerOnlyCheckpoint(milestone: Milestone) {
  const checkpointType = milestone.reviewSummary?.checkpointType || milestone.checkpointType;
  return checkpointType === "prebuild_checkpoint" && Boolean(milestone.preBuildCheckpoint?.outcome) && !milestone.reviewRequest;
}

function checkpointSummaryCopy(input: {
  summary?: MilestoneReviewSummary | null;
  preBuildCheckpoint?: PreBuildCheckpointView | null;
  checkpointState: ReturnType<typeof deriveReviewCheckpointState>;
}) {
  if (input.preBuildCheckpoint?.outcome && input.preBuildCheckpoint.outcome !== "match") {
    return input.preBuildCheckpoint.summary || input.summary?.latestSubmissionSummary || "Build is blocked until the repo and stack contract are resolved.";
  }
  if (input.summary?.latestSubmissionSummary) return input.summary.latestSubmissionSummary;
  if (input.preBuildCheckpoint?.summary) return input.preBuildCheckpoint.summary;

  switch (input.checkpointState.key) {
    case "awaiting_submission":
      return "No review packet has been submitted yet.";
    case "setup_required":
      return "This checkpoint is blocked on repo setup, so review cannot start yet.";
    case "awaiting_evidence":
      return "A submission exists, but the review evidence is not complete yet.";
    case "changes_requested":
      return "This checkpoint is waiting on updates before it can be approved.";
    case "approved":
      return "This checkpoint is approved and ready to move forward.";
    default:
      return "This checkpoint is ready for review and decision.";
  }
}

function formatCheckpointReason(reason?: string | null) {
  if (!reason) return null;

  return reason
    .replace(/^Manual review required for unsupported requirement kinds:\s*/i, "Needs manual review because these requirement types could not be auto-checked: ")
    .replace(/^Missing required project artifact:\s*/i, "Missing required project artifact: ")
    .replace(/^Code-heavy delivery cannot advance to review or completion without a real GitHub repo linked to the project\.?/i, "Add a real GitHub repo to this project before this checkpoint can be approved.")
    .replace(/^GitHub repo provisioning is in progress for this code-heavy project\. A real repo will still be required before review or completion\.?/i, "GitHub repo setup is still in progress. This checkpoint stays blocked until a real repo is linked.");
}

function formatTaskStatusLabel(value?: string | null) {
  if (!value) return "Unknown";
  return value.replace(/_/g, " ");
}

function taskProgressValue(task: any) {
  if (!task) return null;

  if (task.status === "done" || task.status === "completed") {
    return 100;
  }

  if (typeof task?.progress_pct === "number" && Number.isFinite(task.progress_pct)) {
    return Math.max(0, Math.min(100, task.progress_pct));
  }

  return null;
}

function isTaskExecutionStale(task: { status?: string | null; updated_at?: string | null } | null | undefined) {
  if (!task || task.status !== "in_progress") return false;
  return isStaleExecutionTimestamp(task.updated_at, 1 * 60 * 1000);
}

function isBootstrapTask(task: any, bootstrapSprintIds?: ReadonlySet<string>) {
  return matchesBootstrapTruth(task, bootstrapSprintIds);
}

function formatBytes(value?: number | null) {
  if (!value) return null;
  if (value < 1024) return `${value} B`;
  if (value < 1024 * 1024) return `${Math.round(value / 1024)} KB`;
  return `${(value / (1024 * 1024)).toFixed(1)} MB`;
}

function artifactEmptyState(projectType?: string | null, intake?: any) {
  if (intake?.shape === "launch-campaign" || ["marketing_growth", "marketing"].includes(projectType || "")) {
    return "No campaign links or creative artifacts added yet.";
  }
  if (["product_build", "ops_enablement", "saas", "web_app", "native_app"].includes(projectType || "")) {
    return "No repo, environment, or build links added yet.";
  }
  return "No project links or artifacts added yet.";
}

function formatUpdatedDate(value?: string | null) {
  if (!value) return "Recently updated";

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "Recently updated";

  return `Updated ${date.toLocaleDateString(undefined, {
    month: "short",
    day: "numeric",
    year: "numeric",
  })}`;
}

