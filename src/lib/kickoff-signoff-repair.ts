type DbClient = {
  from: (table: string) => any;
};

const SIGNOFF_PHASE_KEYS = new Set(["design", "build", "message", "validate"]);
const SIGNOFF_TASK_TYPES = new Set(["design", "build_implementation", "content_messaging", "qa_validation"]);

function shouldRepairTask(task: any) {
  const phaseKey = typeof task?.task_metadata?.phase_key === 'string' ? task.task_metadata.phase_key : null;
  const taskType = typeof task?.task_type === 'string' ? task.task_type : null;
  const autoGenerated = task?.task_metadata?.auto_generated === true;

  return autoGenerated && (
    (phaseKey && SIGNOFF_PHASE_KEYS.has(phaseKey))
    || (taskType && SIGNOFF_TASK_TYPES.has(taskType))
  );
}

export async function repairKickoffSignoffTasks(db: DbClient, input: { projectId: string }) {
  const { data: tasks, error } = await db
    .from('sprint_items')
    .select('id, task_type, review_required, review_status, task_metadata')
    .eq('project_id', input.projectId);

  if (error) {
    throw new Error(error.message || 'Failed to inspect project kickoff tasks for signoff repair');
  }

  const candidates = (tasks || []).filter((task: any) => shouldRepairTask(task) && task.review_required !== true);
  if (candidates.length === 0) {
    return { repaired: 0 } as const;
  }

  const ids = candidates.map((task: any) => task.id).filter(Boolean);
  if (ids.length === 0) {
    return { repaired: 0 } as const;
  }

  const { error: updateError } = await db
    .from('sprint_items')
    .update({
      review_required: true,
      review_status: 'awaiting_review',
    })
    .in('id', ids);

  if (updateError) {
    throw new Error(updateError.message || 'Failed to repair kickoff signoff tasks');
  }

  return { repaired: ids.length } as const;
}
